#define _CRT_SECURE_NO_WARNINGS 1 

#include <stdio.h>

union 
{
	int c;
	char a[4];
}s = { .a[0] = 0x01, .a[1] = 0x02, .a[2] = 0x03, .a[3] = 0x04 };

// 乱序赋初值
// 联合体共用一块空间
// 如果以大端字节序存储
// 高字节的值存放在低地址处
// 那么存入内存就是
// 0x 01 02 03 04
// 当用16进制打印 c 时
// 取出前两个字节的空间
// 值为 0x0102

// 小端字节序则是：
// 0x 01 02 03 04
// 取出来顺序颠倒
// 0x 2010
// 

int main()
{
	printf("%x", s.c);
	return 0;
}